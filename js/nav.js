//start navigation function
$('#open-nav').click(function() {
	$('header').removeClass('hidden-mobile');
	$('#open-nav').addClass('hidden');
	$('#close-nav').removeClass('hidden');
});

$('#close-nav').click(function() {
	$('header').addClass('hidden-mobile');
	$('#open-nav').removeClass('hidden');
	$('#close-nav').addClass('hidden');
});
//end navigation function

